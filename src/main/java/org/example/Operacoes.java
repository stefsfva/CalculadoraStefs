package org.example;

public class Operacoes {

    //Método ou Função que soma dois números.
    public double soma (double a, double b) {
        return a + b;
    }

    //Método ou Função que subrai dois números.
    public double subtrair (double a, double b) {
    return a - b;
    }

    //Método ou Função que multiplica dois números.
    public double multiplicar (double a, double b) {
        return a * b;
    }

    //Método ou Função que divide dois números.
    public double dividir (double a, double b) {
        return a / b;
    }
}


